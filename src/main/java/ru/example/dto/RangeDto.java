package ru.example.dto;

public class RangeDto {
    private Long bt;
    private String mn;
    private String mx;
    private String dt;

    public Long getBt() {
        return bt;
    }

    public void setBt(Long bt) {
        this.bt = bt;
    }

    public String getMn() {
        return mn;
    }

    public void setMn(String mn) {
        this.mn = mn;
    }

    public String getMx() {
        return mx;
    }

    public void setMx(String mx) {
        this.mx = mx;
    }

    public String getDt() {
        return dt;
    }

    public void setDt(String dt) {
        this.dt = dt;
    }
}
